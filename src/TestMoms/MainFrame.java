    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String searchtext = jTextField1.getText();
        int keyword = jComboBox1.getSelectedIndex();
        String options[] = {"name", "country", "state", "city", "interest"};
        String sql;
        if ("".equals(searchtext) && keyword < 4) {
            sql = "Select * from  person";

        } else if ("".equals(searchtext) && keyword == 4) {
            sql = "Select * from interests ORDER BY interest_id ASC";
        } else if (keyword == 4) {
            sql = "Select * from interests where interest_name = \'" + searchtext + "\'";
        } else {
            sql = "Select * from person where " + options[keyword]
                    + " = \'" + searchtext + "\'";
        }
        DefaultListModel model = new DefaultListModel();
        jList1.setModel(model);
        ResultSet rs = null;
        try {
            con = DriverManager.getConnection(url, user, password);
            st = con.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addElement(rs.getString(1) + " " + rs.getString(2));
            }
        } catch (SQLException ex) {
            Logger lgr = Logger.getLogger(Version.class.getName());
            lgr.log(Level.SEVERE, ex.getMessage(), ex);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (st != null) {
                    st.close();
                }
                if (con != null) {
                    con.close();
                }

            } catch (SQLException ex) {
                Logger lgr = Logger.getLogger(Version.class.getName());
                lgr.log(Level.WARNING, ex.getMessage(), ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


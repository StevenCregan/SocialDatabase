    private void valueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_valueChanged
        String sql;
        String value = "";
        String childrenNames = "";
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        int keyword = jComboBox1.getSelectedIndex();
        if (!jList1.isSelectionEmpty() && keyword < 4) {
            value = jList1.getSelectedValue().toString();
            value = value.replaceAll("[^\\d]", "");
            sql = "Select * from person LEFT JOIN Person_Child on (person.person_id = Person_Child.person_id) LEFT JOIN Child on (Person_Child.child_id = child.child_id) where person.person_id = " + value;

            ResultSet rs = null;
            try {
                con = DriverManager.getConnection(url, user, password);
                st = con.createStatement();
                rs = st.executeQuery(sql);
                while (rs.next()) {
                    jTextField2.setText(rs.getString(2));
                    jTextField3.setText(rs.getString(3));
                    jTextField4.setText(rs.getString(4));
                    jTextField5.setText(rs.getString(5));
                    if (rs.getString(6) != null) {
                        if (childrenNames != "") {
                            childrenNames = (childrenNames + ", \n" + rs.getString(9));
                        } else {
                            childrenNames = rs.getString(9);
                        }
                    }
                    jTextField6.setText(childrenNames);
                }
            } catch (SQLException ex) {
                Logger lgr = Logger.getLogger(Version.class.getName());
                lgr.log(Level.SEVERE, ex.getMessage(), ex);
            } finally {
                try {
                    if (rs != null) {
                        rs.close();
                    }
                    if (st != null) {
                        st.close();
                    }
                    if (con != null) {
                        con.close();
                    }

                } catch (SQLException ex) {
                    Logger lgr = Logger.getLogger(Version.class.getName());
                    lgr.log(Level.WARNING, ex.getMessage(), ex);
                }
            }
        } else;
    }//GEN-LAST:event_valueChanged

